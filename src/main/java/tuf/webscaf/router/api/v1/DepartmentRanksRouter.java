    public RouterFunction<ServerResponse> departmentRankRoutes(DepartmentRankHandler handle) {
        return RouterFunctions.route(GET("academic/api/v1/department-ranks/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::index).filter(new IndexDepartmentRankHandlerFilter())
                .and(RouterFunctions.route(GET("academic/api/v1/department-ranks/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::show).filter(new ShowDepartmentRankHandlerFilter()))
                .and(RouterFunctions.route(POST("academic/api/v1/department-ranks/store").and(accept(APPLICATION_FORM_URLENCODED)), handle::store).filter(new StoreDepartmentRankHandlerFilter()))
                .and(RouterFunctions.route(PUT("academic/api/v1/department-ranks/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::update).filter(new UpdateDepartmentRankHandlerFilter()))
                .and(RouterFunctions.route(PUT("academic/api/v1/department-ranks/status/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::status).filter(new ShowDepartmentRankHandlerFilter()))
                .and(RouterFunctions.route(DELETE("academic/api/v1/department-ranks/delete/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::delete).filter(new ShowDepartmentRankHandlerFilter()));
    }

